export interface SurpriseResult {
  topic: InspirationTopic;
  description: string; // الوصف الكامل المُعدَّل
  announcement: string; // "خيال اختارت لك: ..."
}

/**
 * اختيار موضوع مفاجئ مع مراعاة تاريخ المستخدم لتجنب التكرار
 */
export async function pickSurprise(userId?: string): Promise<SurpriseResult> {
  // قراءة تاريخ المستخدم لتجنب تكرار المجالات
  let avoidDomains: string[] = [];
  if (userId) {
    try {
      const memory = await getMemory(userId);
      // تجنب المجالات الأكثر استخداماً (تنويع التجربة)
      const topDomains = Object.entries(memory.domainHistory)
        .sort((a, b) => b[1] - a[1])
        .slice(0, 2)
        .map(([d]) => d);
      avoidDomains = topDomains;
    } catch { /* تجاهل */ }
  }

  // تصفية المواضيع (تجنب المجالات المتكررة إن أمكن)
  let candidates = INSPIRATION_TOPICS;
  if (avoidDomains.length > 0) {
    const filtered = INSPIRATION_TOPICS.filter(t => !avoidDomains.includes(t.domain));
    if (filtered.length >= 5) candidates = filtered;
  }

  // اختيار عشوائي
  const topic = candidates[Math.floor(Math.random() * candidates.length)];

  // إضافة لمسة إبداعية فريدة
  const creativeAngles = [
    "من منظور طائر يحلق عالياً",
    "في لحظة الذروة العاطفية",
    "بعيون طفل يرى هذا لأول مرة",
    "في الساعة الذهبية قبيل الغروب",
    "بأسلوب سينمائي احترافي",
    "في لحظة صمت مطبق",
    "حين يلتقي الماضي بالحاضر",
    "من زاوية لم يرها أحد من قبل",
  ];
  const angle = creativeAngles[Math.floor(Math.random() * creativeAngles.length)];

  const description = `${topic.description} — ${angle}`;
  const announcement = `${topic.domainEmoji} خيال اختارت لك: "${topic.title}"`;

  return { topic, description, announcement };
}

